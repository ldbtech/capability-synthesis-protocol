    import matplotlib.patches as mpatches
    import numpy as np
    import io
    import base64
    
    # Read input parameters
    learning_rate = args.get('learning_rate', 0.01)
    iterations = args.get('iterations', 50)
    initial_x = args.get('initial_x', 5.0)
    initial_y = args.get('initial_y', 5.0)
    
    # Define a simple quadratic loss function: f(x, y) = (x-1)^2 + (y-2)^2
    # Gradient: df/dx = 2(x-1), df/dy = 2(y-2)
    def loss_function(x, y):
        return (x - 1)**2 + (y - 2)**2
    
    def gradient(x, y):
        grad_x = 2 * (x - 1)
        grad_y = 2 * (y - 2)
        return grad_x, grad_y
    
    # Initialize
    x, y = initial_x, initial_y
    frames = []
    history = [(x, y, loss_function(x, y))]
    
    # Run gradient descent with step capture
    for step in range(iterations):
        grad_x, grad_y = gradient(x, y)
        x = x - learning_rate * grad_x
        y = y - learning_rate * grad_y
        loss = loss_function(x, y)
        history.append((x, y, loss))
    
    # Sample frames if too many (keep first, last, and evenly spaced)
    if len(history) > 60:
        indices = set([0, len(history) - 1])
        stride = len(history) // 58
        indices.update(range(0, len(history), stride))
        indices = sorted(list(indices))
        sampled_history = [history[i] for i in indices]
    else:
        sampled_history = history
    
    # Create contour plot background (loss landscape)
    x_range = np.linspace(-2, 8, 100)
    y_range = np.linspace(-2, 8, 100)
    X, Y = np.meshgrid(x_range, y_range)
    Z = (X - 1)**2 + (Y - 2)**2
    
    # Render each frame
    for frame_idx, (curr_x, curr_y, curr_loss) in enumerate(sampled_history):
        fig, ax = plt.subplots(figsize=(7, 4), dpi=100)
        
        # Draw contour plot
        contour = ax.contour(X, Y, Z, levels=15, cmap='viridis', alpha=0.6)
        ax.clabel(contour, inline=True, fontsize=8)
        
        # Draw trajectory so far
        hist_x = [h[0] for h in sampled_history[:frame_idx + 1]]
        hist_y = [h[1] for h in sampled_history[:frame_idx + 1]]
