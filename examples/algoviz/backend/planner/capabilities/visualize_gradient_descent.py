        # Draw current point
        ax.plot(curr_x, curr_y, 'ro', markersize=10, label='Current position', zorder=5)
        
        # Draw optimum
        ax.plot(1, 2, 'g*', markersize=20, label='Optimum (1, 2)', zorder=5)
        
        ax.set_xlabel('x')
        ax.set_ylabel('y')
        ax.set_title(f'Gradient Descent Step {frame_idx} | Loss: {curr_loss:.4f}')
