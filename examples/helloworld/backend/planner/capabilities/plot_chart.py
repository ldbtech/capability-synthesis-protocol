    agg_map = {
        'mean': 'mean',
        'sum': 'sum',
        'count': 'count',
        'median': 'median',
        'min': 'min',
        'max': 'max'
    }
    agg_method = agg_map.get(agg_func, 'mean')
    
    if series_col and series_col in columns:
        # Multi-series aggregation
        grouped = df.groupby([x_col, series_col])[y_col].agg(agg_method).reset_index()
        pivot_data = grouped.pivot(index=x_col, columns=series_col, values=y_col)
        plot_data = pivot_data
    else:
        # Single series aggregation
        plot_data = df.groupby(x_col)[y_col].agg(agg_method).sort_values(ascending=False)
    
    # Create figure
    fig, ax = plt.subplots(figsize=(10, 6))
    
    # Plot based on chart kind
    if chart_kind == 'bar':
        if isinstance(plot_data, pd.DataFrame):
            plot_data.plot(kind='bar', ax=ax)
        else:
            plot_data.plot(kind='bar', ax=ax, color='steelblue')
