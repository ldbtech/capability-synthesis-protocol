    import matplotlib.pyplot as plt
    import io
    import base64
    from collections import defaultdict
    
    # Extract inputs
    rows = args.get('rows', [])
    columns = args.get('columns', [])
    
    # Chart configuration (general parameters for any chart)
    chart_kind = args.get('kind', 'bar')  # bar, line, scatter, histogram, box, etc.
    x_col = args.get('x', None)
    y_col = args.get('y', None)
    series_col = args.get('series', None)
    agg_func = args.get('agg', 'mean')  # mean, sum, count, median, etc.
    title = args.get('title', None)
    xlabel = args.get('xlabel', None)
    ylabel = args.get('ylabel', None)
    
    # For this specific invocation: default to salary by department
    if not x_col:
        x_col = args.get('group_by', 'department')
    if not y_col:
        y_col = args.get('metric', 'salary')
    if not title:
        title = args.get('title', f'{agg_func.capitalize()} {y_col} by {x_col}')
    if not ylabel:
        ylabel = f'{agg_func.capitalize()} {y_col}'
    if not xlabel:
        xlabel = x_col
    
    # Validate columns exist
    if x_col not in columns or y_col not in columns:
        return {
            'error': f'Column not found. Available: {columns}',
            'image_base64': None
        }
    
    # Convert to DataFrame for easier aggregation
    df = pd.DataFrame(rows)
    
    # Clean and parse numeric values defensively
    def parse_numeric(val):
        if pd.isna(val):
            return None
        if isinstance(val, (int, float)):
            return float(val)
        if isinstance(val, str):
            # Remove currency symbols, commas, units
            cleaned = val.replace('$', '').replace(',', '').replace('kms', '').strip()
            try:
                return float(cleaned)
            except ValueError:
                return None
        return None
    
    df[y_col] = df[y_col].apply(parse_numeric)
    
    # Remove rows with missing values in key columns
    df = df.dropna(subset=[x_col, y_col])
    
